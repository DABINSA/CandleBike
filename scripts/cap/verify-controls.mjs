import puppeteer from 'puppeteer-core';
const CHROME = 'C:\\Program Files\\Google\\Chrome\\Application\\chrome.exe';
const OUT = 'C:\\Users\\David\\AppData\\Local\\Temp\\claude\\d--Project-Stock-Game\\1fb0636e-f7cf-4ca4-bcb8-3248f3ca07ef\\scratchpad';
const sleep = (ms) => new Promise((r) => setTimeout(r, ms));
const b = await puppeteer.launch({ executablePath: CHROME, headless: 'new', args: ['--no-sandbox', '--hide-scrollbars'] });
const p = await b.newPage();
p.on('dialog', (d) => d.accept().catch(() => {}));
await p.setViewport({ width: 636, height: 1048, deviceScaleFactor: 1, isMobile: true, hasTouch: true });
await p.goto('https://candlebike.vercel.app/?tune=1', { waitUntil: 'networkidle2', timeout: 60000 });
await sleep(1500);
await p.click('#symbol-input');
await p.type('#symbol-input', 'AAPL', { delay: 80 });
await p.waitForSelector('#search-results li', { timeout: 15000 });
await p.click('#search-results li');
await p.waitForFunction(() => { const x = document.querySelector('#btn-start'); return x && !x.disabled; }, { timeout: 8000 });
await p.click('#btn-start');
await p.waitForFunction(() => document.querySelector('#screen-play')?.classList.contains('active'), { timeout: 30000 });
await p.keyboard.down('ArrowUp');
await sleep(4500);
await p.keyboard.up('ArrowUp');
const dist = await p.evaluate(() => document.querySelector('#hud-distance')?.textContent);
const present = await p.evaluate(() => ['btn-wheelie', 'btn-nose', 'btn-jump', 'btn-gas', 'btn-restart-play', 'tune-panel'].filter((id) => document.getElementById(id)));
await p.screenshot({ path: `${OUT}\\controls.png` });
console.log('dist=', dist, ' present=', present.join(','));
await b.close();
